package entities;

/**
 * 
 * @author Dayinta Warih Wulandari
 */
public class Role {
    private String id;
    private String role;

    public Role(String id, String role) {
        this.id = id;
        this.role = role;
    }

    
    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }
    
}
